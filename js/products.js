const productContainer = document.getElementById("product_container");

for (let i = 0; i < products.length; i++) {
    productContainer.innerHTML += `
 <div class="pasteleria-grupo">
          <img src=${products[i].img} alt="Cannoli" />

          <p class="pasteleria-grupo-encabezado">
            ${products[i].nombre}<br /><i class="fas fa-plus"></i>
          </p>

          <div class="pasteleria-grupo-cuerpo" id=${i}>
          <p>
           ${products[i].descripcion}
            <br />
            <a href="#">Pedir</a>
          </p>
          </div>
        </div>

`

    const cards = document.querySelectorAll('.pasteleria-grupo');
    const descriptions = document.querySelectorAll('.pasteleria-grupo-cuerpo');

    // Agregar evento click a cada tarjeta
    cards.forEach((card, index) => {
        card.addEventListener('click', () => {
            // Verificar si la descripción está visible
            const isVisible = descriptions[index].style.display === 'block';

            // Ocultar todas las descripciones
            descriptions.forEach((desc) => {
                desc.style.display = 'none';
            });

            // Si la descripción no estaba visible, mostrarla
            if (!isVisible) {
                descriptions[index].style.display = 'block';
            }
        });
    });

}

